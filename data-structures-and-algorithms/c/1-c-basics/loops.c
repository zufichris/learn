#include<stdio.h>

int main(){
    int count=0;

    //forloop
printf("For loop running");
for(int i=0;i<10;i++){
       count+=i;// count=count+i
       printf("%d,\t",count);
    }
printf("\n");

    //while loop
    printf("While loop running");
while(count<100){
    count+=1;
    printf("%d,\t",count);
}
printf("\n");
    //do-while
    printf("do-while loop running");
do{
        count+=1;
        printf("%d,\t",count);
    }while(count<200);

    return 0;
}
